public abstract class Pagamento {
    protected double saldo;

    public Pagamento(double saldo) {
        this.saldo = saldo;
    }

    public double getSaldo() {
        return saldo;
    }

    public void setSaldo(double saldo) {
        this.saldo = saldo;
    }
    
    
    public abstract void processarPagamento(double pagamento);
    
}
