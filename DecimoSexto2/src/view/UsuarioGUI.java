    private void btnPorIdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPorIdActionPerformed
        int id = Integer.valueOf(txtId.getText());
        Optional<Usuario> u = usuarioController.getById(id);

        u.ifPresentOrElse(us -> {
            txtNome.setText(us.getNome());
            txtUsuario.setText(us.getUsuario());
        }, () -> {
            JOptionPane.showMessageDialog(this, "ID nao encontrado");
            txtId.setText("");
            txtNome.setText("");
            txtUsuario.setText("");
            txtSenha.setText("");
            txtId.requestFocus();
        });

    }//GEN-LAST:event_btnPorIdActionPerformed

