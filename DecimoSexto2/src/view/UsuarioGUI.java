    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        String nome = txtNome.getText();
        String usuario = txtUsuario.getText();
        char[] senhaArray = txtSenha.getPassword();
        String senha = new String(senhaArray);
        senha = BCrypt.hashpw(senha, BCrypt.gensalt());

        usuarioController.add(nome, usuario, senha);
        JOptionPane.showMessageDialog(this, "Usuario Salvo");
        popularTabela(usuarioController.getAll());
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        int resposta = JOptionPane.showConfirmDialog(this, "Tem ceteza que deseja excluir usuario?", "Confirmas Exclusao",
                JOptionPane.YES_NO_OPTION,
                JOptionPane.WARNING_MESSAGE);
        if (resposta == JOptionPane.YES_OPTION) {
        int id = Integer.valueOf(txtId.getText());
        if (usuarioController.delete(id)) {
            JOptionPane.showMessageDialog(this, "Usuario Excluido com sucesso");
        } else {
            JOptionPane.showMessageDialog(this, "Usuario nao Encontrado");
        }
        popularTabela(usuarioController.getAll());
        } else {
           JOptionPane.showMessageDialog(this, "Nenhuma alteracao feita");
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        int id = Integer.valueOf(txtId.getText());
        String nome = txtNome.getText();
        String usuario = txtUsuario.getText();
        char[] senhaArray = txtSenha.getPassword();
        String senha = new String(senhaArray);
        senha = BCrypt.hashpw(senha, BCrypt.gensalt());
        if (usuarioController.update(id, nome, usuario, senha)) {
            JOptionPane.showMessageDialog(this, "Usuario Alterado");
        } else {
            JOptionPane.showMessageDialog(this, "Usuario nao Encontrado");
        }
        popularTabela(usuarioController.getAll());
    }//GEN-LAST:event_btnAlterarActionPerformed

    private void tblUsuariosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblUsuariosMouseClicked
        int id = (int) tblUsuarios.getValueAt(tblUsuarios.getSelectedRow(),0);
        
       Optional<Usuario> u = usuarioController.getById(id);

        u.ifPresentOrElse(
            us -> {
            txtId.setText(String.valueOf(us.getId()));
            txtNome.setText(us.getNome());
            txtUsuario.setText(us.getUsuario());
        }, () -> {
            JOptionPane.showMessageDialog(this, "ID nao encontrado");
            txtId.setText("");
            txtNome.setText("");
            txtUsuario.setText("");
            txtSenha.setText("");
            txtId.requestFocus();
        });
    }//GEN-LAST:event_tblUsuariosMouseClicked

    public void popularTabela(ArrayList<Usuario> usuarios) {
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("ID:");
        model.addColumn("Nome:");
        model.addColumn("Usuario:");

        for (Usuario u : usuarios) {
            model.addRow(new Object[]{u.getId(), u.getNome(), u.getUsuario()});
        }
        tblUsuarios.setModel(model);
    }

