public abstract class Jogo {
    private String nome;

    public Jogo(String nome) {
        this.nome = nome;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }
    
    public abstract void jogar(); 
    
}
